						Administrativo Nacional, Samaniego y demás distritos vecinos bajaron 
						de categoría quedando como corregimientos del distrito de Túquerres, 
						con derecho de elegir un miembro para esa municipalidad. En 1864, reunida 
						la municipalidad de Túquerres, nuevamente, elevo a distritos a Samaniego 
						y a los corregimientos de Ancuya, Guaitarilla, Sapuyes, Mallama y Yascual.
						El jefe de la municipalidad nombró a don Manuel de J. Benavides para que 
						dirija los destinos de Samaniego, quien debió afrontar épocas de retraso económico y crisis política.

						<p class="post-contenido text-justify font-cs">

							<span class="letter-cs">CAMPAÑA PUBLICITARIA</span><br>

							
							La socialización de puesta en marcha del Proyecto “Letras Viajeras” se hizo a través
							de medios de comunicación locales con la Emisora Digital Estéreo en participación en
							el programa “Hola Sandoná”, redes sociales y medios impresos como afiches, volantes
							y pendones; con el fin de dar a conocer el proyecto, las actividades y los espacios
							que se desarrollan en el transcurso de la ejecución del proyecto. <br><br>

							Fangpace “Letras Viajeras, la magia que abre las puertas de la imaginación”; página
							web aprobada y registrada por el Ministerio de Cultura. <br><br>

							Igualmente la socialización del proyecto se ha hecho de manera personal
							visitando cada una de las Instituciones a las cuales se han llevado las actividades,
							tanto en el sector rural como en el casco urbano.							
						
						</p>
						<br>
					</article>

					<article class="post clearfix">


						<center><span class="titlos">INICIO DE ACTIVIDADES:</span></center><br>
						<p class="post-contenido text-justify font-cs">

							<span class="letter-cs">Primera Fase: PRIMERA INFANCIA</span><br>

							<b>NOMBRE DE LA ACTIVIDAD:</b> Cine Club “Imaginemos” <br>
							<b>DIRIGIDA A:</b> Niños y niñas de entre 0 a 7 años. <br>
							<b>DURACIÓN:</b> Inicio 15 de Marzo – 30 de Abril de 2016. <br>
							<b>LUGARES A VISITAR:</b> Todas las Instituciones Educativas, Centros Educativos, Hogares Infantiles y Centros de Desarrollo Infantil tanto del Casco Urbano como el Sector Rural. <br>
							<b>DESCRIPCIÓN:</b> Creación de un cine club de películas infantiles de corta duración dirigido a niños y niñas entre los 0 y 8 años, que permitan el espacio para la reflexión el dialogo y la retro alimentación de lo observado. <br>							
						
						</p>
						<br>

						<p class="post-contenido text-justify font-cs">

							<span class="letter-cs">Objetivos:</span><br>

							<ol class="post-contenido text-justify font-cs">
								<li>Dar a conocer a los Niño y niñas de manera práctica y didáctica según su edad sobre los derechos, deberes y responsabilidades de que son sujeto.</li><br>
								<li>Es propiciar la participación en público, incentivar habilidades artísticas, fortalecer valores de respeto, tolerancia y convivencia. </li><br>
								<li>Los niños y niñas desarrollen habilidades interpretativas y argumentativas desde
									la lectura en sus diversas estrategias de enseñanza.
									La fase de realización de los Talleres de “Letras Viajeras” con niños y niñas de entre
									los 0 y 7 años es fundamental tener en cuenta que la sistematización y metodología de
									las actividades, deben realizarse en un lenguaje técnico especial para los niños y niñas
									que se encuentran en la etapa de primera infancia.
								</li>
							</ol>
						</p>
							

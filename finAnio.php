							<b>31 de Diciembre:</b> <br><br>				
							El último día de cada año, la comunidad se congrega entorno al concurso de años viejos.
							Mientras la gente arma, de manera sencilla, unos muñecos de ropas viejas rellenos de aserrín o papel,
						    los artesanos más hábiles hacen unos muñecos de mayor tamaño y de mayor calidad, generalmente con papel mache,
						    para participar en el concurso que realiza la Alcaldía Municipal. Estos son más pintorescos, hechos con gran 
						    ingenio y creatividad. El año viejo por lo general hace referencia a un hecho que sucedió en el año que termina.
						     Quienes se inscriben al concurso deben desfilar por las principales calles de la ciudad para que un jurado
						      defina cuál es el mejor. Este gana un premio especial en dinero. Los años viejos que hace la gente los pasean,
						       durante el día, por las calles; en la noche los exhiben en el frente de las casas y unos minutos antes de 
						       la media noche son quemados para significar el fin del año. El agüero de la gente dice que al quemar el
						        año viejo se quema todo lo malo y se recibe el nuevo año con esperanza y positivismo.  <br><br>
						</p>

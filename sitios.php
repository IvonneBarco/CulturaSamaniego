						<p class="post-contenido text-justify font-cs myfont">
							
							En pleno centro de la ciudad el templo parroquial se convierte en 
							un atractivo de interés no sólo religioso sino también arquitectónico. 
							Este templo es la renovación de uno primero que permaneció durante 89
							 años al lado izquierdo del actual. Si para la construcción del primer 
							 templo fueron alma y nervio don Simón Álvarez (Fundador de Samaniego) 
							 y su familia, la segunda la impulsaron un señor Pueyo de Val, 
							 diocesano de Pasto y el Padre Herman Klinschmidt, director espiritual 
							 de la parroquia y de la obra.<br>
							Los planos fueron elaborados en 1900 por el arquitecto alemán Antonio 
							Doering en colaboración con monseñor Pedro Schumacher, circunstancia que 
							lleva a concluir que éste proyectaba levantar el nuevo templo, dado su 
							dinamismo y su cariño por esta tierra. Su construcción fue lenta, se inició en 
							1918 y se inauguró el 8 de junio de 1937 dentro de la programación del centenario 
							de Samaniego. Su atractivo está no sólo en la historia de su construcción sino 
							también en el diseño arquitectónico y las imágenes religiosas que hay en su interior. 
							Además allí se encuentra el mausoleo del obispo Monseñor Pedro Schumacher, donde 
							se conservan los restos de este ilustre personaje, que está en proceso de convertirse en Santo.
						</p>

				 protegerán su patria chica. Entre las estrellas y el puma se encuentra 
				 la palabra SAMANIEGO que significa grandeza. Como fondo se destaca el 
				 paisaje Andino con recursos propios de nuestra geografía y el confín donde
				 se besan el infinito cielo con las ariscas montañas, sus faldas y vallados
				 en policroma colcha de parcelas características de un municipio minifundista.
				 Por el centro un río símbolo de la riqueza hídrica abundante aún en su territorio. 
				 en el flanco derecho esta representada una mano protectora y sobre su cuenco un árbol
				 y un arbusto símbolo de agro y flora. en el lado izquierdo se representa un zapapico 
				 sobre un bloque de cristales de roca símbolo de la riqueza aurífera. el vértice inferior
				 del escudo contiene los lemas FRATERNIDAD, INNOVACION Y TRABAJO sobre fondo de oro símbolo de nobleza.
				 El lema traduce una trilogía de fuerzas que convergen a un centro y a la vez se revierten en 
				 consecución de progreso, tres puntas de flecha que concurren con el amarillo de fondo y tres de
				 color verde que salen. De los bordes de la punta nacen dos alas blancas que se pliegan cubriendo 

					<article class="post clearfix">

						<h2 class="post-title">
							<p class="titulos"><u>PROYECTO LETRAS VIAJERAS</u></p>
						</h2>
						<br>
						<p class="post-contenido text-justify font-cs">
							

							<i>“LETRAS VIAJERAS”</i> Proyecto financiado por el Ministerio de Cultura mediante
							el Programa Nacional Concertación, que facilita, apoya, reconoce y promueve los diversos
							procesos de creación, investigación, formación y manifestaciones culturales fomentando
							las iniciativas culturales locales que contribuyen al afianzamiento de la democracia,
							al reconocimiento y la formación de valores culturales, generando sentido de pertenencia
							por un entorno social. <br><br>

							Proyecto asignado y aprobado mediante Convenio de Apoyo de Actividades Artísticas y Culturales
							<b>Número 0514/16</b>; suscrito entre el Municipio de Sandoná y el Ministerio de Cultura.
							Recursos asignados: <b>$34.000.000.</b> Con fecha de inicio de ejecución desde el <i>2 de Febrero
							de 2016 hasta el 02 de Septiembre</i> del mismo año.							
						</p>
						
						<br>
							<h3 class="label-cs"><b>OBJETO:</b></h3><br>
							<p class="post-contenido text-justify font-cs">								
								El proyecto “Letras Viajeras” consiste en la dinamización de la Biblioteca Pública de Sandoná
								como un servicio de extensión y se constituye en una estrategia para llegar a grupos de población
								con dificultades de acceso al servicio de biblioteca enfatizando la dificultad en el sector rural.
								<br>
								Se ejecutaran actividades tendientes a promocionar los hábitos de lectura, el interés por la escritura,
								la apreciación cinematográfica y el aprendizaje para el manejo básico de las herramientas informáticas,
								con el uso integral de los recursos que actualmente se cuenta, tales como la sala de informática, la serie
								audiovisual y la colección bibliográfica. <br>
							</p>
							
							<h3 class="label-cs"><b>PLANEACIÓN:</b></h3><br>
							<p class="post-contenido text-justify font-cs">								
								La Dirección del Proyecto “Letras Viajeras” está bajo la Alcaldía Municipal en Cabeza del Doctor Byron Zambrano Rosas 
								y la Coordinación estará a cargo de la Oficina de la Coordinación de Cultura y la Biblioteca Municipal; para el desarrollo 
								colectivo y exitoso  establece articulación con gestores culturales que conformaron el Equipo de Trabajo e Instituciones 
								Educativas y Formativas municipales que serían cooperantes y beneficiarios; igualmente se formula el plan de acción y c
								ronograma de trabajo donde se determinan las actividades y los tiempos que se ejecutaran para cumplimiento del objeto del proyecto. <br>
							</p>
							
							<h3 class="label-cs"><b>PLAN DE TRABAJO:</b></h3><br>
							<p class="post-contenido text-justify font-cs">								
								Tomando los puntos centrales que se han plasmado dentro de la formulación del proyecto se ha construido 
								un plan de trabajo tratando de cubrir el 100% del aspecto geográfico del municipio, procurando llevar las 
								actividades y talleres a toda la comunidad tanto en el sector rural como en el urbano; sectorizando a la 
								comunidad por grupos poblacionales (primera infancia, infancia, adolescencia, jóvenes y adultos, adultos 
								mayores y personas en situación de discapacidad) y aplicando una política inclusiva y participativa <br><br>

								El Plan de Trabajo está construido para desarrollarse en seis fases,
								una por cada grupo poblacional  y se organizan actividades específicas 
								estructuradas en una síntesis, objetivos y talleres cada una en orientación a promover 
								la lectura por intermedio de la diversidad de las artes como el teatro, música, títeres, 
								cine, entre otros y convirtiendo estos espacios en una herramienta de integración familiar y social.<br><br>

								A continuación se detallan las actividades a desarrollarse:<br><br>
							</p>
							<p class="post-contenido text-justify font-cs">
								<ul class="post-contenido text-justify font-cs">
								    <li><span class="titlos">PRIMERA FASE:</span></li><br>
								    	<span class="letter-cs">“Cine Club Imaginemos”:</span><br>
										<b>DIRIGIDO A:</b> Primera Infancia – Niños y niñas entre los 0 – 7 años<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> Creación de un Cine Club de películas infantiles de corta duración
										dirigido a los niños y niñas entre los 0 y 7 años, que permitan el espacio para la reflexión el
										dialogo y la retro alimentación de lo observado.<br><br>

								    <li><span class="titlos">SEGUNDA FASE:</span></li> <br>
								    	<span class="letter-cs">“Club de Lectura Pequeños Lectores”</span><br>
										<b>DIRIGIDO A:</b> Infancia – Niños y niñas entre los 8 – 11 años<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> Creación de un club de lectura de obras literarias infantiles
										cuentos, fabulas, poesía, novelas) que permiten la inclinación a la lectura interpretativa,
										argumentativa y analítica a nivel de la edad de los asistentes, motivando también a la escritura.<br><br>

									<li><span class="titlos">TERCERA FASE:</span></li> <br>
										<span class="letter-cs">"Cine Club Cinergia"</span><br>
										<b>DIRIGIDO A:</b> Adolescentes – Niños y niñas entre los 12 – 16 años<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> Creación de un Club de apreciación cinematográfica con piezas de 
										genero de animación o acción, en una mezcla de muestras artísticas que tengan un contenido 
										reflexivo y de promoción de valores, de derechos, deberes y oportunidades que hay dentro del
										entorno que se desenvuelve esta población.<br><br>

									<li><span class="titlos">CUARTA FASE:</span></li> <br>
										<span class="letter-cs">Taller de Manejo Básico de Herramientas Informáticas</span><br>
										<b>DIRIGIDO A:</b>	Comunidad en general<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> Alfabetizar a los adultos principalmente aquellos 
										que no conocen el manejo y acceso a las tecnologías, al igual que para aquellas 
										personas que las saben utilizar, enseñar y promocionar y aplicar las prácticas del buen uso de las TIC´S.<br><br>

										<span class="letter-cs">"Lunada"</span><br>
										<b>DIRIGIDO A:</b> Jóvenes y Adultos<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> Adecuar espacios apropiados para que los artistas 
										sandoneños muestren sus habilidades artísticas en muestra de cultura y educación, generando iniciativa a la integración social.<br><br>


									<li><span class="titlos">QUINTA FASE:</span></li> <br>
										<span class="letter-cs">Tertulias Literarias“Los libros también tienen voz”</span><br>
										<b>DIRIGIDO A:</b> Adultos Mayores. Hombres y Mujeres de los 55 años en adelante.<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> Buscar estrategias que vinculen a los adultos mayores
										y personas en situación de discapacidad a participar de las diferentes actividades 
										lúdicas que lleven a la educación, formación y recreación; como tertulias literarias
										que involucren al núcleo familiar mediante métodos y herramientas según el grupo poblacional caracterizado.<br><br>

									<li><span class="titlos">SEXTA FASE:</span></li> <br>
										<span class="letter-cs">"Libros Turistas"</span><br>
										<b>DIRIGIDO A:</b> Comunidad en General<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> La actividad tiene como fin llevar parte de la biblioteca Municipal
										a las Instituciones Educativas y Centros Educativos, Veredas y Corregimientos sector Rural del 
										Municipio, personas quienes en su mayoría tienen dificultad de acceso a los servicios que se ofrece en la Biblioteca.<br><br>
										
										<span class="letter-cs">"Concurso de Lectura – SANDONA LEE"</span><br>
										<b>DIRIGIDO A:</b> Comunidad en General<br>
										<b>DESCRIPCIÓN DE LA ACTIVIDAD:</b> Reconocimiento y estimulación a los participantes del concurso, 
										permitiendo mostrar las aptitudes propias de lectura y escritura en todos los grupos poblacionales del 
										municipio de Sandoná.<br>
								</ul>
							</p>

